    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        try {
            String selectedItem = cmbMenuItem.getSelectedItem().toString();
            int quantity = Integer.parseInt(txtOrderQuantity.getText());

            String[] itemParts = selectedItem.split(" - ");
            int itemId = Integer.parseInt(itemParts[0]);
            String itemName = itemParts[1];

            Connection con = DBConnection.getConnection();
            String sql = "SELECT price, quantity FROM menu_items WHERE item_id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, itemId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                double unitPrice = rs.getDouble("price");
                double total = unitPrice * quantity;

                DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
                model.addRow(new Object[]{
                    itemId,
                    itemName,
                    quantity,
                    unitPrice,
                    total
                });

                calculateGrandTotal();
                txtOrderQuantity.setText("");
            }
            int availableQty = rs.getInt("quantity");

            if (quantity > availableQty) {
                JOptionPane.showMessageDialog(this, "Not enough stock available!");
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnAddToCartActionPerformed

    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        try {
            String selectedCustomer = cmbCustomer.getSelectedItem().toString();
            String[] customerParts = selectedCustomer.split(" - ");
            int customerId = Integer.parseInt(customerParts[0]);

            LocalDate orderDate = LocalDate.now();
            LocalTime orderTime = LocalTime.now();

            double grandTotal = Double.parseDouble(txtGrandTotal.getText());
            double subtotal = grandTotal;
            double discount = 0.0;

            Connection con = DBConnection.getConnection();

            String orderSql = "INSERT INTO orders(customer_id, order_date, order_time, subtotal, discount, grand_total) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement orderPst = con.prepareStatement(orderSql, Statement.RETURN_GENERATED_KEYS);

            orderPst.setInt(1, customerId);
            orderPst.setDate(2, java.sql.Date.valueOf(orderDate));
            orderPst.setTime(3, java.sql.Time.valueOf(orderTime));
            orderPst.setDouble(4, subtotal);
            orderPst.setDouble(5, discount);
            orderPst.setDouble(6, grandTotal);

            orderPst.executeUpdate();

            ResultSet generatedKeys = orderPst.getGeneratedKeys();
            int orderId = 0;

            if (generatedKeys.next()) {
                orderId = generatedKeys.getInt(1);
            }

            DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();

            for (int i = 0; i < model.getRowCount(); i++) {
                int itemId = Integer.parseInt(model.getValueAt(i, 0).toString());
                int quantity = Integer.parseInt(model.getValueAt(i, 2).toString());
                double unitPrice = Double.parseDouble(model.getValueAt(i, 3).toString());
                double totalPrice = Double.parseDouble(model.getValueAt(i, 4).toString());

                String itemSql = "INSERT INTO order_items(order_id, item_id, quantity, unit_price, total_price) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement itemPst = con.prepareStatement(itemSql);

                itemPst.setInt(1, orderId);
                itemPst.setInt(2, itemId);
                itemPst.setInt(3, quantity);
                itemPst.setDouble(4, unitPrice);
                itemPst.setDouble(5, totalPrice);

                itemPst.executeUpdate();
                String updateStockSql = "UPDATE menu_items SET quantity = quantity - ? WHERE item_id = ?";
                PreparedStatement stockPst = con.prepareStatement(updateStockSql);
                stockPst.setInt(1, quantity);
                stockPst.setInt(2, itemId);
                stockPst.executeUpdate();
            }

            StringBuilder invoice = new StringBuilder();

            invoice.append("        ISLAND FLAVORS RESTAURANT\n");
            invoice.append("========================================\n");
            invoice.append("Order ID: ").append(orderId).append("\n");
            invoice.append("Customer: ").append(selectedCustomer).append("\n");
            invoice.append("Date: ").append(orderDate).append("\n");
            invoice.append("Time: ").append(orderTime.withNano(0)).append("\n");
            invoice.append("========================================\n");
            invoice.append(String.format("%-15s %-8s %-10s\n", "Item", "Qty", "Total"));
            invoice.append("========================================\n");

            for (int i = 0; i < model.getRowCount(); i++) {
                if (model.getValueAt(i, 0) == null) {
                    continue;
                }

                String itemName = model.getValueAt(i, 1).toString();
                String qty = model.getValueAt(i, 2).toString();
                String total = model.getValueAt(i, 4).toString();

                invoice.append(String.format("%-15s %-8s %-10s\n", itemName, qty, total));
            }

            invoice.append("========================================\n");
            invoice.append("Grand Total: ").append(grandTotal).append("\n");
            invoice.append("========================================\n");
            invoice.append("      Thank you for visiting us!\n");

            InvoiceForm invoiceForm = new InvoiceForm(invoice.toString());
            invoiceForm.setVisible(true);

            JOptionPane.showMessageDialog(this, "Order placed successfully!");

            ((DefaultTableModel) tblOrder.getModel()).setRowCount(0);
            txtGrandTotal.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

