    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed
        int selectedRow = tblOrder.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an item to remove!");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        model.removeRow(selectedRow);

        calculateGrandTotal();
    }//GEN-LAST:event_btnRemoveItemActionPerformed
    
    public void loadMenuItemsByFoodType() {
        try {
            Connection con = DBConnection.getConnection();

            String selectedType = cmbFilterFoodType.getSelectedItem().toString();
            String sql;

            if (selectedType.equals("All")) {
                sql = "SELECT item_id, item_name FROM menu_items";
            } else {
                sql = "SELECT item_id, item_name FROM menu_items WHERE food_type = ?";
            }

            PreparedStatement pst = con.prepareStatement(sql);

            if (!selectedType.equals("All")) {
                pst.setString(1, selectedType);
            }

            ResultSet rs = pst.executeQuery();

            cmbMenuItem.removeAllItems();

            while (rs.next()) {
                cmbMenuItem.addItem(rs.getInt("item_id") + " - " + rs.getString("item_name"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }    
    
    public void calculateGrandTotal() {
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        double grandTotal = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            Object value = model.getValueAt(i, 4);

            if (value != null) {
                grandTotal += Double.parseDouble(value.toString());
            }
        }

        txtGrandTotal.setText(String.valueOf(grandTotal));
    }
    
    
